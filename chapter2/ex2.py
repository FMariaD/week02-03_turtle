
import turtle as t

t.shape('turtle')
t.speed(1)
t.color('green')

T0 = [(0, 0), (0, -100), (-50, -100), (-50, 0), (0, 0)]
T1 = [(0, 0), (0, -100), (0, 0), (-50, -50)]
T2 = [(0, 0), (-50, 0), (0, 0), (0, -50), (-50, -100), (0, -100)]
T3 = [(0, 0), (-50, 0), (0, 0), (-50, -50), (0, -50), (-50, -100)]
T4 = [(0, -100), (0, -50), (-50, -50), (-50, 0)]
T5 = [(0, 0), (-50, 0), (-50, -50), (0, -50), (0, -100), (-50, -100)]
T6 = [(0, 0), (-50, 0), (-50, -50), (-50, -100), (0, -100), (0, -50), (-50, -50)]
T7 = [(0, 0), (-50, 0), (0, 0), (-50, -50), (-50, -100)]
T8 = [(0, 0), (0, -100), (-50, -100), (-50, -50), (0, -50), (-50, -50), (-50, 0), (0, 0)]
T9 = [(0, 0), (-50, 0), (-50, -50), (0, -50), (0, 0), (0, -50), (-50, -100)]

start = [-200, 0]
def draw_number(xy, start):
    t.penup()
    t.goto(*start)
    t.pendown()
    x0, y0 = start
    for x, y in xy:
        t.goto(x + x0, y + y0)

for i in [T1, T4, T1, T7, T0, T0]:
    draw_number(i, start)
    start[0] += 100
