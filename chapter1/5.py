import turtle as tur

tur.shape('turtle')
tur.color('blue')
a = 20
c = -10
tur.speed(2)

for i in range (0, 10):
    
    for j in range (0, 4):
        tur.forward(a)
        tur.left(90)
        j += 1
        
    tur.penup()
    tur.goto(c, c)
    tur.pendown()
    a += 20
    c -= 10
    i += 1
